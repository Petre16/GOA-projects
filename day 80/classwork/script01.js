// Day of the Week Message

// Write a switch case that takes a number (1–7) and logs the corresponding day of the week (e.g., 1 = "Monday").

let weekday = 3;

switch  (weekday) {
    case 1:
        console.log("Monday");
        break
    case 2:
        console.log("Tuesday");
        break
    case 3:
        console.log("Wednesday");
        break
    case 4:
        console.log("Thursday");
        break
    case 5:
        console.log("Friday");
        break
    case 6:
        console.log("Saturday");
        break
    case 7:
        console.log("Sunday");
        break
    default:
        console.log("I dont know this day");
}